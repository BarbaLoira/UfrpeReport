package br.com.ufrpe.ws.dto;

public class FiltroResume {
	private String LugarFiltrado;
	private String SituacaoFiltrado;

	public String getLugarFiltrado() {
		return LugarFiltrado;
	}

	public void setLugarFiltrado(String lugarFiltrado) {
		LugarFiltrado = lugarFiltrado;
	}

	public String getSituacaoFiltrado() {
		return SituacaoFiltrado;
	}

	public void setSituacaoFiltrado(String situacaoFiltrado) {
		SituacaoFiltrado = situacaoFiltrado;
	}

}
